def ast_stats(ast):
    funcs = 0
    stmts = 0

    for node in ast.get("children", []):
        if node.get("type") == "ContractDefinition":
            for sub in node.get("subNodes", []):
                if sub.get("type") == "FunctionDefinition":
                    funcs += 1
                    body = sub.get("body")
                    if isinstance(body, dict):
                        stmts += len(body.get("statements", []))
                    elif isinstance(body, list):
                        stmts += len(body)
    return funcs, stmts

