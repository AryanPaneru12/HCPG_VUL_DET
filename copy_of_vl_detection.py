def draw_hcpg(h):
    plt.figure(figsize=(9, 6))

    # Stronger spacing for heterogeneous graph
    pos = nx.spring_layout(h, seed=10, k=2.8)

    function_nodes = []
    stmt_nodes = []

    for n, a in h.nodes(data=True):
        if a.get("node_type") == "function":
            function_nodes.append(n)
        else:
            stmt_nodes.append(n)

    # Draw function nodes
    nx.draw_networkx_nodes(
        h, pos,
        nodelist=function_nodes,
        node_color="#85C1E9",
        node_size=2000,
        label="Function Nodes"
    )

    # Draw statement nodes
    nx.draw_networkx_nodes(
        h, pos,
        nodelist=stmt_nodes,
        node_color="#F7DC6F",
        node_size=1000,
        label="Statement Nodes"
    )

    # Draw edges
    nx.draw_networkx_edges(
        h, pos,
        edge_color="gray",
        arrows=True,
        arrowsize=12,
        width=1.2
    )

    # Draw labels
    nx.draw_networkx_labels(
        h, pos,
        font_size=7
    )

    plt.title("Partial Heterogeneous Code Property Graph", fontsize=12)
    plt.legend(scatterpoints=1)
    plt.axis("off")
    plt.tight_layout()
    plt.show()

