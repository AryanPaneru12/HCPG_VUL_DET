def extract_cfg(ast):
    cfg = nx.DiGraph()
    idx = 0

    for node in ast.get("children", []):
        if node.get("type") == "ContractDefinition":
            for sub in node.get("subNodes", []):
                if sub.get("type") == "FunctionDefinition":
                    prev = None
                    body = sub.get("body")

                    if isinstance(body, dict):
                        stmts = body.get("statements", [])
                    elif isinstance(body, list):
                        stmts = body
                    else:
                        continue

                    for stmt in stmts:
                        curr = f"stmt_{idx}"
                        cfg.add_node(curr, node_type="statement")
                        if prev:
                            cfg.add_edge(prev, curr, edge_type="control")
                        prev = curr
                        idx += 1
    return cfg

