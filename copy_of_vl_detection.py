def merge_graphs(call_g, cfg):
    h = nx.DiGraph()

    for g in [call_g, cfg]:
        for n, a in g.nodes(data=True):
            h.add_node(n, **a)
        for u, v, a in g.edges(data=True):
            h.add_edge(u, v, **a)

    return h

